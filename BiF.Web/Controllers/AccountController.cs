using System.Linq;
using System.Security.Claims;
using System.Threading.Tasks;
using System.Web;
using System.Web.Mvc;
using BiF.DAL.Models;
using BiF.Web.Owin;
using Microsoft.AspNet.Identity;
using Microsoft.AspNet.Identity.Owin;
using Microsoft.Owin.Security;
using BiF.Web.ViewModels.Account;

namespace BiF.Web.Controllers
{
    [Authorize]
    public class AccountController : BaseController
    {
        private BifSignInManager _signInManager;
        private BifUserManager _userManager;

        public AccountController() { }

        public AccountController(BifUserManager userManager, BifSignInManager signInManager )
        {
            UserManager = userManager;
            SignInManager = signInManager;
        }

        public BifSignInManager SignInManager {
            get => _signInManager ?? HttpContext.GetOwinContext().Get<BifSignInManager>();
            private set => _signInManager = value;
        }

        public BifUserManager UserManager
        {
            get => _userManager ?? HttpContext.GetOwinContext().GetUserManager<BifUserManager>();
            private set => _userManager = value;
        }

        //
        // GET: /Account/Login
        [AllowAnonymous]
        public ActionResult Login(string returnUrl)
        {
            ViewBag.ReturnUrl = returnUrl;
            return View();
        }

        //
        // POST: /Account/Login
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Login(LoginVM model, string returnUrl) {
            if (!ModelState.IsValid)
                return View(model);

            // This doesn't count login failures towards account lockout
            // To enable password failures to trigger account lockout, change to shouldLockout: true
            SignInStatus result = await SignInManager.PasswordSignInAsync(model.Email, model.Password, model.RememberMe, shouldLockout: false);
            switch (result) {
                case SignInStatus.Success:
                    return redirectToLocal(returnUrl);
                case SignInStatus.LockedOut:
                    return View("Lockout");
                case SignInStatus.RequiresVerification:
                    return RedirectToAction("SendCode", new { ReturnUrl = returnUrl, RememberMe = model.RememberMe });
                case SignInStatus.Failure:
                default:
                    ModelState.AddModelError("", "Invalid login attempt.");
                    return View(model);
            }
        }
        
